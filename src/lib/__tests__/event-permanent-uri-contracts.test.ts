import assert from "node:assert/strict";
import { readFileSync } from "node:fs";
import { test } from "node:test";
import path from "node:path";

const root = process.cwd();

function readSource(filePath: string) {
  return readFileSync(path.join(root, filePath), "utf8");
}

test("events expose integration URI and QR code routes", () => {
  const helperSource = readSource("src/lib/event-share-links.ts");
  const permanentRouteSource = readSource("src/app/e/[id]/page.tsx");
  const qrRouteSource = readSource("src/app/api/events/[id]/qrcode/route.ts");
  const dashboardSource = readSource("src/app/dashboard/AdminDashboardWorkspace.tsx");
  const courseDetailSource = readSource("src/app/courses/[id]/page.tsx");
  const openMatDetailSource = readSource("src/app/open-mats/[id]/page.tsx");
  const publicDetailSource = readSource("src/components/PublicEventDetailPage.tsx");
  const inlineDirectionsSource = readSource("src/components/InlineDirectionsButton.tsx");
  const mapWithDirectionSource = readSource("src/components/MapWithDirection.tsx");
  const publicListingWarningSource = readSource("src/components/PublicListingWarning.tsx");
  const spotCounterSource = readSource("src/components/SpotCounter.tsx");
  const publicFlyerSource = readSource("src/components/PublicEventFlyer.tsx");
  const eventSummaryTagSource = readSource("src/components/EventSummaryTag.tsx");
  const summaryTileSource = readSource("src/components/SummaryTile.tsx");
  const bookEventButtonSource = readSource("src/components/BookEventButton.tsx");
  const panelContextSource = readSource("src/components/PanelContext.tsx");
  const warningPanelSource = readSource("src/components/WarningPanel.tsx");
  const coursesSource = readSource("src/lib/courses.ts");

  assert.match(helperSource, /eventPermanentPath/);
  assert.match(helperSource, /`\/e\/\$\{eventId\}`/);
  assert.match(helperSource, /eventPermanentUrl/);
  assert.match(helperSource, /NEXTAUTH_URL/);
  assert.match(permanentRouteSource, /eventPublicPath/);
  assert.match(permanentRouteSource, /redirect\(eventPublicPath\(event\)\)/);
  assert.match(qrRouteSource, /QRCode\.toString/);
  assert.match(qrRouteSource, /eventPermanentUrl\(id\)/);
  assert.match(qrRouteSource, /image\/svg\+xml/);
  assert.match(dashboardSource, /Integration URI/);
  assert.match(dashboardSource, /QR Code/);
  assert.match(dashboardSource, /eventQrCodePath\(event\.id\)/);
  assert.match(dashboardSource, /InlineDirectionsButton/);
  assert.match(courseDetailSource, /eventPermanentPath\(event\.id\)/);
  assert.match(courseDetailSource, /eventQrCodePath\(event\.id\)/);
  assert.match(courseDetailSource, /PublicEventDetailPage/);
  assert.doesNotMatch(courseDetailSource, />Open URI</);
  assert.match(openMatDetailSource, /eventPermanentPath\(event\.id\)/);
  assert.match(openMatDetailSource, /eventQrCodePath\(event\.id\)/);
  assert.match(openMatDetailSource, /PublicEventDetailPage/);
  assert.doesNotMatch(openMatDetailSource, />Open URI</);
  assert.match(publicDetailSource, /aria-label="Event QR code"/);
  assert.doesNotMatch(publicDetailSource, />Event QR Code</);
  assert.doesNotMatch(publicDetailSource, /permanentUrl/);
  assert.match(publicDetailSource, /qrCodeHref/);
  assert.match(publicDetailSource, /MapWithDirection/);
  assert.match(publicDetailSource, /SummaryTile/);
  assert.ok(publicDetailSource.indexOf("<PublicEventFlyer") < publicDetailSource.indexOf("<PublicListingWarning"));
  assert.ok(publicDetailSource.indexOf("<PublicListingWarning") < publicDetailSource.indexOf("<dl className=\"grid gap-3"));
  assert.ok(publicDetailSource.indexOf("<dl className=\"grid gap-3") < publicDetailSource.indexOf("<BookingAction"));
  assert.match(publicDetailSource, /BookEventButton/);
  assert.match(bookEventButtonSource, /Omit<ButtonProps, "children">/);
  assert.match(bookEventButtonSource, /loading/);
  assert.match(bookEventButtonSource, /return "Book"/);
  assert.match(bookEventButtonSource, /text-lg font-black/);
  assert.match(eventSummaryTagSource, /SummaryTile as EventSummaryTag/);
  assert.match(summaryTileSource, /export type SummaryTileProps/);
  assert.match(summaryTileSource, /PanelContext/);
  assert.match(summaryTileSource, /className/);
  assert.match(summaryTileSource, /pin/);
  assert.match(summaryTileSource, /text-base font-normal uppercase leading-5/);
  assert.match(summaryTileSource, /text-base font-normal leading-5/);
  assert.doesNotMatch(summaryTileSource, /font-black|font-semibold|text-xs/);
  assert.match(panelContextSource, /export type PanelContextProps/);
  assert.match(panelContextSource, /pin\?: ReactNode/);
  assert.match(panelContextSource, /p-3/);
  assert.match(publicListingWarningSource, /WarningPanel/);
  assert.match(warningPanelSource, /export type WarningPanelProps/);
  assert.match(warningPanelSource, /title: ReactNode/);
  assert.match(warningPanelSource, /children: ReactNode/);
  assert.match(warningPanelSource, /tone\?: WarningPanelTone/);
  assert.match(mapWithDirectionSource, /InlineDirectionsButton/);
  assert.match(mapWithDirectionSource, /google\.com\/maps/);
  assert.match(mapWithDirectionSource, /output=embed/);
  assert.match(mapWithDirectionSource, /<iframe/);
  assert.match(publicFlyerSource, /defaultFlyerImage\.png/);
  assert.doesNotMatch(publicFlyerSource, /SpotCounter/);
  assert.match(spotCounterSource, /bookedCount/);
  assert.match(spotCounterSource, /availableSpots/);
  assert.match(spotCounterSource, /20\)/);
  assert.doesNotMatch(spotCounterSource, /border-stone-300|shadow-md|bg-white/);
  assert.match(spotCounterSource, /text-xs font-black leading-5/);
  assert.match(spotCounterSource, /spots available/);
  assert.match(inlineDirectionsSource, /directionsUrl\(trimmedAddress\)/);
  assert.match(inlineDirectionsSource, /target="_blank"/);
  assert.match(inlineDirectionsSource, /rel="noreferrer"/);
  assert.match(inlineDirectionsSource, /size="sm"/);
  assert.match(coursesSource, /occurrenceDateParam \? startOfDay/);
  assert.match(coursesSource, /: startOfDay\(now\)/);
});
